@router.get("/statistics")
def pipeline_statistics():
    """
    Returns a complete snapshot of the current pipeline statistics.

    Response Fields:
    ---------------
    frame_index

    detections
        Number of objects detected in the latest processed frame.

    raw_detection_records
        Total number of detections recorded since the current
        pipeline session started.

    unique_vehicles
        Number of unique tracker IDs (vehicles/objects)
        observed during the current session.

    flow_statistics
        Statistics generated by the vehicle flow module.

        vehicle_distribution
            Total count of each vehicle class that has crossed
            configured counting lines.

        vehicle_scene
            Current number of each vehicle class visible
            in the frame.

        lane_distribution
            Lane-wise count of vehicles that have crossed
            each configured counting line.

        lane_scene
            Current vehicle count for each lane.

    density_statistics
        Statistics generated by the density estimation module.

        track_history
            Historical centroid positions for every tracked
            object. Used to generate density heatmaps.

        last_positions
            Most recent centroid position of each tracked object.

        heatmap_available
            Indicates whether a density heatmap has been
            initialized and is available.

        heatmap_shape
            Resolution of the generated density heatmap.

    speed_statistics
        Statistics generated by the speed estimation module.

        class_speed
            Average estimated speed grouped by vehicle class.

        time_speed
            Instantaneous speed history indexed over time
            or frame intervals.
    """
    stats = manager.get_statistics()
    return stats


