@router.post("/start")
def start_pipeline(request: PipelineStartRequest):
    """
    Initializes and starts a complete video processing pipeline.

    Steps:
        1. Reset previous session
        2. Save UI configuration
        3. Convert selected class names into model class IDs
        4. Create the processing pipeline and video source
        5. Register runtime objects with the pipeline manager
        6. Start video capture and background processing
    """

    # ------------------------------------------------------------------
    # Reset any previous pipeline session.
    # ------------------------------------------------------------------
    manager.reset()

    # ------------------------------------------------------------------
    # Save current UI configuration to disk so it persists across restarts.
    # ------------------------------------------------------------------
    manager.current_config = {
        "camera_type": request.camera_type,
        "det_mode": request.det_mode,
        "confidence_threshold": request.confidence_threshold,
        "iou_threshold": request.iou_threshold,
        "frame_rate": request.frame_rate,
        "classes_to_detect": request.classes_to_detect,
    }

    try:
        with open(CONFIG_PATH, "w") as f:
            json.dump(manager.current_config, f, indent=5)
        logger.info(f"Config saved: {manager.current_config}")
    except Exception:
        logger.exception("Config save failed")

    # ------------------------------------------------------------------
    # Convert selected class names into model-specific class IDs.
    # ------------------------------------------------------------------
    model_map = MODEL_MAPS[request.camera_type]

    cls_map = {v: k for k, v in CLASS_MAPS[request.camera_type].items()}

    selected_class_ids = [
        CLASS_MAPS[request.camera_type][class_name]
        for class_name in request.classes_to_detect
        if class_name in CLASS_MAPS[request.camera_type]
    ]

    # ------------------------------------------------------------------
    # Create the processing pipeline and RTSP video source.
    # ------------------------------------------------------------------
    pipeline = VideoProcessingPipeline(
        det_mode=request.det_mode, model_map=model_map, frame_rate=request.frame_rate
    )

    video_source = VideoSource(source=request.rtsp_url, target_fps=request.frame_rate)

    # ------------------------------------------------------------------
    # Register all runtime objects with the global pipeline manager.
    # ------------------------------------------------------------------
    manager.set_pipeline(pipeline)
    manager.set_class_map(cls_map)
    manager.set_detection_thresholds(
        request.confidence_threshold, request.iou_threshold
    )
    manager.create_session(request.camera_type)
    manager.set_video_source(video_source)

    # ------------------------------------------------------------------
    # Start video capture and background processing.
    # ------------------------------------------------------------------
    video_source.start()
    manager.set_selected_classes(selected_class_ids)
    manager.start_processing()

    return {
        "status": "started",
        "camera_type": request.camera_type,
        "det_mode": request.det_mode,
    }


@router.post("/stop")
def stop_pipeline():
    """
    Stops the currently running pipeline session, releasing
    the video source and any associated processing resources.
    """
    if manager.video_source is not None:
        manager.video_source.stop()

    manager.stop_processing()
    manager.close_session()

    return {"status": "stopped"}


